package day1;

public class Thrusday {
	public static int add(int a, int b) {
		int i = a - 1;
		return i;
	}

	public static double add(double d, double e) {
		double x = d + e;
		return x;
	}

	public static void main(String[] args) {
		int z = add(-20, 19);
		System.out.println(z);
		double y = add(20.3, 20.5);
		System.out.println(y);
		
	}
}
